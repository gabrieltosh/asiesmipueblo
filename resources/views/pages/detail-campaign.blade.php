                                                                    agradezco con todo mi corazón, millón de gracias…”</a>
                                                            </h3>
                                                            <!-- Post Body-->
                                                            <div class="post-body">
                                                                <p>El Alto 30 de enero de 2023. Gracias a la ayuda solidaria
                                                                    de centenares de personas que habitan en Bolivia y otros
                                                                    países, la familia Luna Mamani dejó atrás el mal momento
                                                                    que atravesaban y miran con esperanza el futuro.</p>
                                                                <p>Doña Lucy madre de seis menores, a tiempo de recibir la
                                                                    ayuda solidaria emocionada dijo “Les agradezco con todo
                                                                    mi corazón, millón de gracias, cuando llegué al Programa
                                                                    (Así Es Mi Pueblo) no tenía ni un centavo, mi esposo y
                                                                    mi hija estaban muy enfermos y ustedes nos han ayudado,
                                                                    millón de gracias a todos por ayudarnos”</p>
                                                                <p>Hace tres semanas doña Lucy y sus hijos fueron
                                                                    entrevistados por el periodista Eliser Roca, muy
                                                                    afligidos contaron que se encontraban sin alimentos en
                                                                    su hogar, el padre de familia y una de las hijas estaban
                                                                    tendidos en su casa sin atención médica.</p>
                                                                <p>Entre lágrimas, doña Lucy dijo que se dedicaba a pintar y
                                                                    embolsar palitos de helado y le pagan dos bolivianos por
                                                                    docena, dinero que no le alcanzaba ni siquiera para el
                                                                    alquiler de la casa en la que vivián, pidió que le
                                                                    ayuden con un capital económico que le permita tener un
                                                                    negocio de venta de comida.</p>
                                                                <p>Ante esta triste realidad de una familia de ocho
                                                                    integrantes, se inicio la campaña denominada “Una madre
                                                                    de seis niños pide ayuda para dar de comer a sus hijos,
                                                                    su esposo y una de las niñas están enfermos”.</p>
                                                                <p>Desde muy temprano los días 21 y 28 de enero de 2023, en
                                                                    instalaciones de Canal 57, la familia recibió de manera
                                                                    directa la ayuda de la población, quintales, arrobas y
                                                                    libras de arroz, azúcar y harina, bolsas de fideo,
                                                                    aceite, cereales, ropa, juguetes, entre otros.</p>
                                                                <p>Además, se logró reunir 10.907 bolivianos en la cuenta
                                                                    del banco Unión y 7.795 bolivianos de personas anónimas,
                                                                    haciendo un total de 18.702 bolivianos. </p>
                                                                <p>De este monto se invirtió 9.290 bolivianos en la compra
                                                                    de una cocina industrial, brostera, conservador de
                                                                    pollo, un juego de ollas, platos, cucharas,
                                                                    refrigerador, menaje de cocina, sillas, mesas, carpa y
                                                                    cochecito.</p>
                                                                <p>Todo lo mencionado se entregó de manera pública a doña
                                                                    Lucy y toda su familia, en el programa Así es mi Pueblo
                                                                    de Canal 57.1 y Tele Estrella de El Alto, el día 28 de
                                                                    enero de 2023.</p>
                                                                <p>La niña Belinda a nombre de sus hermanitos dijo “Estoy
                                                                    muy feliz, muchas gracias tíos y tías por ayudarnos,
                                                                    ahora mi mamá nos va a comprar útiles, carnecita para
                                                                    cocinarnos y le ayudaremos a vender comida”</p>
                                                                <p>Don Omar, padre de familia que se encontraba postrado en
                                                                    la cama, que ahora recibe tratamiento de fisioterapia en
                                                                    el Gabinete Monte de Dragón, aseveró “agradezco primero
                                                                    al Señor y a todos quienes me han colaborado en este mal
                                                                    momento que estoy atravesando, gracias hermanos de
                                                                    Bolivia y el exterior.</p>
                                                                <p>Gracias a la valiosa ayuda de muchos corazones
                                                                    solidarios, una vez más logramos transformar las
                                                                    lágrimas de impotencia de una madre de familia, su
                                                                    esposo y seis niños, en sonrisas de alegría y esperanza.
                                                                    ´</p>
                                                                <p>¡Gracias Dios, misión cumplida…!</p>
